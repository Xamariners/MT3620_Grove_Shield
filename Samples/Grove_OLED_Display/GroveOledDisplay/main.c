void DisplayReading(float reading, char *prefix, int sleep)
{
	char buf[50];

	sprintf(buf, "%f", reading);

	char result[100];
	strcpy(result, prefix);
	strcat(result, buf);

	putString(result);

	usleep(sleep);
}


/// <summary>
///     Main entry point for this sample.
/// </summary>
int main(int argc, char *argv[])
{
    Log_Debug("Application starting\n");


    // Register a SIGTERM handler for termination requests
    struct sigaction action;
    memset(&action, 0, sizeof(struct sigaction));
    action.sa_handler = TerminationHandler;
    sigaction(SIGTERM, &action, NULL);

	// INIT GROVE
	int i2cFd;
	GroveShield_Initialize(&i2cFd, 230400);

	// INIT OLED	
	GroveOledDisplay_Init(i2cFd, SH1107G);
	
	//// INIT TEMP
	void* tempsensor = GroveTempHumiSHT31_Open(i2cFd);	

    while (!terminationRequested) {
        Log_Debug("Temp Reader\n");

		// Bitmap display
		clearDisplay();
		drawBitmap(Logo128x128, sizeof(Logo128x128));

		// Word display
		clearDisplay();
		setNormalDisplay();
		setVerticalMode();

		for (uint8_t i = 0; i < 16; i++)
		{
			setTextXY(i, 0);  //set Cursor to ith line, 0th column
			setGrayLevel(i); //Set Grayscale level. Any number between 0 - 15.

			// Read temp from sensors
			GroveTempHumiSHT31_Read(tempsensor);
			float temp = GroveTempHumiSHT31_GetTemperature(tempsensor);
			float humi = GroveTempHumiSHT31_GetHumidity(tempsensor);	

			if(i % 2 == 0)
				DisplayReading(temp, "temp:", 100000);
			else
				DisplayReading(humi, "humi:", 100000);
		}

		usleep(2000000);
    }

    Log_Debug("Application exiting\n");
    return 0;
}

